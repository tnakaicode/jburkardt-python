def components_2d ( A ):

#*****************************************************************************80
#
## components_2d() assigns contiguous nonzero pixels to a common component.
#
#  Discussion:
#
#    The A array contains values of 0 or 1.
#
#    The 0 pixels are to be ignored.  The 1 pixels are to be grouped
#    into connected components.
#
#    The pixel A(I,J) is "connected" to the pixels A(I-1,J), A(I+1,J),
#    A(I,J-1) and A(I,J+1), so except at the boundaries, pixels have 
#    4 neighbors.
#
#    The approach is somewhat naive, but has the advantage of simplicity,
#    and is easily translated into other languages.  In particular, it
#    does not rely on the label() or bwlabel() functions available in
#    some languages (but not all), provided as black-box functions for
#    computing components.
#
#  Picture:
#
#    Input A:
#
#      0  2  0  0 17  0  3
#      0  0  3  0  1  0  4
#      1  0  4  8  8  0  7
#      3  0  6 45  0  0  0
#      3 17  0  5  9  2  5
#
#    Output:
#
#      C:
#
#      0  1  0  0  2  0  3
#      0  0  2  0  2  0  3
#      4  0  2  2  2  0  3
#      4  0  2  2  0  0  0
#      4  4  0  2  2  2  2
#
#  Licensing:
#
#    This code is distributed under the MIT license.
#
#  Modified:
#
#    25 February 2025
#
#  Author:
#
#    John Burkardt
#
#  Input:
#
#    integer A(M,N), the pixel array.
#
#  Output:
#
#    integer C(M,N), the component array.
#
  import numpy as np

  m, n = A.shape 
#
#  Initialization.
#
  C = np.zeros ( [ m, n ], dtype = int )
  component_index = 0
  plist = []
#
#  Check every nonzero data value in A which has not yet
#  been assigned a component.
#
  for i2 in range ( 0, m ):
    for j2 in range ( 0, n ):

      if ( A[i2,j2] != 0 and C[i2,j2] == 0 ):
        plist.append ( i2 )
        plist.append ( j2 )
        component_index = component_index + 1

        while ( plist ):

          j = plist.pop ( )
          i = plist.pop ( )
          C[i,j] = component_index 

          if ( 0 <= i-1 and A[i-1,j] != 0 and C[i-1,j] == 0 ):
            plist.append ( i - 1 )
            plist.append ( j )
          if ( i+1 <= m - 1 and A[i+1,j] != 0 and C[i+1,j] == 0 ):
            plist.append ( i + 1 )
            plist.append ( j )
          if ( 0 <= j - 1 and A[i,j-1] != 0 and C[i,j-1] == 0 ):
            plist.append ( i )
            plist.append ( j - 1 )
          if ( j+1 <= n-1 and A[i,j+1] != 0 and C[i,j+1] == 0 ):
            plist.append ( i )
            plist.append ( j + 1 )

  return C

