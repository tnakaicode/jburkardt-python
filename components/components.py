def plot_C ( C ):

#*****************************************************************************80
#
## plot_C() plots a set of 2D components.
#
#  Licensing:
#
#    This code is distributed under the MIT license.
#
#  Modified:
#
#    24 February 2025
#
#  Author:
#
#    John Burkardt
#
#  Input:
#
#    integer C[m,n]: an array of 0's (blocked) and positive values
#    (labels for component).
#
  import matplotlib.pyplot as plt
  import numpy as np

  m, n = C.shape 

  component_num = np.max ( C )
#
#  Choose a random RGB color for every component, plus 1 for the blocks.
#
  color = np.random.random ( [ component_num + 1, 3 ] )
  color[0,:] = [ 1.0, 1.0, 1.0 ]
  for i in range ( 1, component_num + 1 ):
    color[i,:] = color[i,:] / np.linalg.norm ( color[i,:] )

  plt.clf ( )
  plt.axis ( 'equal' )
  plt.axis ( 'off' )

  outline = True
  for i in range ( 0, m ):
    for j in range ( 0, n ):
      c = C[i,j]
      rgb = color[c,:]
      cell_ij_fill ( m, i, j, rgb, outline )

  plt.title ( 'percolation components' )
  filename = 'components_2d.png'
  plt.savefig ( filename )
  print ( '  Graphics saved as "' + filename + '"' )
  plt.close ( )
  return

def cell_ij_fill ( m, i, j, rgb, outline ):

#*****************************************************************************80
#
## cell_ij_fill() plots a filled (I,J) cell.
#
#  Discussion:
#
#    We assume the data is represented in a matrix.
#
#    In order to convert between the matrix coordinates and picture
#    coordinates, the (I,J) cell will be drawn with the following corners:
#
#    (j-1,m-i+1), (j,m-i+1), (j,m-i), (j-1,m-1).
#
#  Licensing:
#
#    This code is distributed under the MIT license.
#
#  Modified:
#
#    17 July 2022
#
#  Author:
#
#    John Burkardt
#
#  Input:
#
#    integer M, the maximum row index.
#
#    integer I, J, the index of the cell.
#
#    color RGB, can be any of the 8 abbreviated color terms
#    'r', 'g', 'b', 'c', 'm', 'y', 'w', 'k', or an RGB triple such as 
#    [1.0,0.4,0.0].  The square is filled with this color.
#
#    bool outline: is True if the cell should be outlined in black.
#
  import matplotlib.pyplot as plt

  a = j - 1
  b = j
  c = m - ( i - 1 )
  d = m - i

  plt.fill ( [ a, b, b, a ], [ c, c, d, d ], color = rgb )

  if ( outline ):
    plt.plot ( [ a, b, b, a, a ], [ c, c, d, d, c ], 'k-' )

  return

def components_3d ( A ):

#*****************************************************************************80
#
## components_3d() assigns contiguous nonzero pixels to a common component.
#
#  Discussion:
#
#    On input, the A array contains values of 0 or 1.
#
#    The 0 pixels are to be ignored.  The 1 pixels are to be grouped
#    into connected components.
#
#    The pixel A(I,J,K) is "connected" to the pixels:
#
#      A(I-1,J,  K  ),  A(I+1,J,  K  ),
#      A(I,  J-1,K  ),  A(I,  J+1,K  ),
#      A(I,  J,  K-1),  A(I,  J,  K+1),
#
#    so most pixels have 6 neighbors.
#
#    On output, COMPONENT_NUM reports the number of components of nonzero
#    data, and the array C contains the component assignment for
#    each nonzero pixel, and is 0 for zero pixels.
#
#  Licensing:
#
#    This code is distributed under the MIT license.
#
#  Modified:
#
#    12 March 2025
#
#  Author:
#
#    John Burkardt
#
#  Input:
#
#    integer L, M, N, the order of the array.
#
#    integer A(L,M,N), the pixel array.
#
#  Output:
#
#    integer C(L,M,N), the component array.
#
  import numpy as np

  l, m, n = A.shape
#
#  Initialization.
#
  C = np.zeros ( [ l, m, n ], dtype = int )
  component_num = 0
  plist = []
#
#  Check every nonzero data value in A which has not yet
#  been assigned a component.
#
  for i2 in range ( 0, l ):
    for j2 in range ( 0, m ):
      for k2 in range ( 0, n ):

        if ( A[i2,j2,k2] != 0 and C[i2,j2,k2] == 0 ):
          plist.append ( i2 )
          plist.append ( j2 )
          plist.append ( k2 )
          component_num = component_num + 1

          while ( plist ):

            k = plist.pop ( )
            j = plist.pop ( )
            i = plist.pop ( )
            C[i,j,k] = component_num 

            if ( 0 <= i-1 and A[i-1,j,k] != 0 and C[i-1,j,k] == 0 ):
              plist.append ( i - 1 )
              plist.append ( j )
              plist.append ( k )
            if ( i+1 <= l - 1 and A[i+1,j,k] != 0 and C[i+1,j,k] == 0 ):
              plist.append ( i + 1 )
              plist.append ( j )
              plist.append ( k )
            if ( 0 <= j - 1 and A[i,j-1,k] != 0 and C[i,j-1,k] == 0 ):
              plist.append ( i )
              plist.append ( j - 1 )
              plist.append ( k )
            if ( j+1 <= m-1 and A[i,j+1,k] != 0 and C[i,j+1,k] == 0 ):
              plist.append ( i )
              plist.append ( j + 1 )
              plist.append ( k )
            if ( 0 <= k - 1 and A[i,j,k-1] != 0 and C[i,j,k-1] == 0 ):
              plist.append ( i )
              plist.append ( j )
              plist.append ( k - 1 )
            if ( k+1 <= n-1 and A[i,j,k+1] != 0 and C[i,j,k+1] == 0 ):
              plist.append ( i )
              plist.append ( j )
              plist.append ( k + 1 )

  return C

