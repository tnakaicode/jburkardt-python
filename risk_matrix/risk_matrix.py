  A = risk_adjacency_matrix ( )

  m, n = A.shape

  T = np.zeros ( [ m, n ], dtype = float )
  for i in range ( 0, m ):
    rowsum = np.sum ( A[i,:], dtype = float )
    print ( '  i = ', i, '  rowsum = ', rowsum )
